import java.util.Scanner;

public class SubtractionGame {
    public static void main(String ARGS[]) {
        Scanner sc = new Scanner(System.in);
        int t=sc.nextInt();
        for(int i=0;i<t;i++){
            int a=sc.nextInt();
            int b=sc.nextInt();
            System.out.println(a+b);

        }
    }
}

